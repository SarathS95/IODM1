// function for addition
function add(a, b) {
    return a + b;
}

// function for subtraction
function sub(a, b) {
    return a - b;
}

// function for multiplication
function mul(a, b) {
    return a * b;
}

// function for division
function divide(a, b) {
    return a / b;
}

// Call the function
console.log(sum(10, 20));
console.log(sub(30, 15));
console.log(mul(105, 20));
console.log(divide(100, 10));